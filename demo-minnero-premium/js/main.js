// === LANGUAGE MANAGEMENT ===
let currentLang = localStorage.getItem('ly-lang') || 'no';

function t(key) {
  return (translations[currentLang] && translations[currentLang][key]) ||
         (translations['no'][key]) || key;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = t(key);
    if (el.tagName === 'IMG') {
      el.alt = val;
    } else if (el.hasAttribute('placeholder')) {
      el.placeholder = val;
    } else {
      el.textContent = val;
    }
  });

  document.documentElement.lang = currentLang === 'no' ? 'nb' : 'en';

  const toggleEl = document.getElementById('lang-toggle');
  const mobileToggleEl = document.getElementById('mobile-lang-toggle');
  if (toggleEl) toggleEl.textContent = t('nav.lang');
  if (mobileToggleEl) mobileToggleEl.textContent = t('nav.lang');
}

function toggleLanguage() {
  currentLang = currentLang === 'no' ? 'en' : 'no';
  localStorage.setItem('ly-lang', currentLang);
  applyTranslations();
}

function initLangToggle() {
  document.getElementById('lang-toggle')?.addEventListener('click', toggleLanguage);
  document.getElementById('mobile-lang-toggle')?.addEventListener('click', toggleLanguage);
}

// === NAVIGATION ===
function initNav() {
  const nav = document.querySelector('.nav');
  if (nav) {
    window.addEventListener('scroll', () => {
      nav.classList.toggle('scrolled', window.scrollY > 10);
    }, { passive: true });
  }

  const hamburger = document.getElementById('hamburger');
  const mobileMenu = document.getElementById('mobile-menu');

  if (hamburger && mobileMenu) {
    const closeMenu = () => {
      mobileMenu.classList.remove('open');
      hamburger.classList.remove('open');
      hamburger.setAttribute('aria-expanded', 'false');
    };

    hamburger.addEventListener('click', e => {
      e.stopPropagation();
      const isOpen = mobileMenu.classList.toggle('open');
      hamburger.classList.toggle('open', isOpen);
      hamburger.setAttribute('aria-expanded', isOpen);
      // Body scroll stays unlocked — the liquid-glass panel only covers
      // 320px of the viewport, so the rest of the page should remain
      // scrollable behind it.
    });

    // Close when a link inside is tapped
    mobileMenu.querySelectorAll('a').forEach(link => {
      link.addEventListener('click', closeMenu);
    });

    // Close when user clicks anywhere outside the panel
    document.addEventListener('click', e => {
      if (!mobileMenu.classList.contains('open')) return;
      if (mobileMenu.contains(e.target) || hamburger.contains(e.target)) return;
      closeMenu();
    });

    // Close on Escape
    document.addEventListener('keydown', e => {
      if (e.key === 'Escape' && mobileMenu.classList.contains('open')) closeMenu();
    });
  }

  const currentPage = window.location.pathname.split('/').pop() || 'index.html';
  document.querySelectorAll('.nav-links a, .mobile-menu a').forEach(link => {
    const href = link.getAttribute('href');
    if (href && href === currentPage) link.classList.add('active');
    if (currentPage === '' && href === 'index.html') link.classList.add('active');
  });
}

// === SCROLL ANIMATIONS ===
function initScrollAnimations() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12, rootMargin: '0px 0px -40px 0px' });

  document.querySelectorAll('.reveal').forEach(el => observer.observe(el));
}

// === PREMIUM — gentle photo fade-in (longer threshold than .reveal) ===
function initPhotoFade() {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.18, rootMargin: '0px 0px -60px 0px' });

  document.querySelectorAll('.fade-in, .interstitial').forEach(el => observer.observe(el));
}

// === PREMIUM — very subtle ambient parallax on hero === */
function initAmbientParallax() {
  const heroBg = document.querySelector('.hero-bg[data-parallax]');
  if (!heroBg) return;
  if (window.matchMedia('(prefers-reduced-motion: reduce)').matches) return;

  const speed = parseFloat(heroBg.dataset.parallax) || 0.25;
  let ticking = false;

  function update() {
    const y = window.scrollY * speed;
    heroBg.style.setProperty('--parallax-y', `${y}px`);
    ticking = false;
  }

  window.addEventListener('scroll', () => {
    if (!ticking) {
      requestAnimationFrame(update);
      ticking = true;
    }
  }, { passive: true });
}

// === PREMIUM — memorial wall renderer ===
async function initMemorialWall() {
  const root = document.getElementById('memorial-wall-root');
  if (!root) return;

  try {
    const res = await fetch('data/memorials.json', { cache: 'no-store' });
    if (!res.ok) throw new Error('fetch failed');
    const data = await res.json();
    const memorials = Array.isArray(data.memorials) ? data.memorials : [];

    if (!memorials.length) {
      root.innerHTML = `<p class="memorial-empty">${t('memorial.empty')}</p>`;
      return;
    }

    // Group by year of passing, newest first
    const byYear = {};
    memorials.forEach(m => {
      const year = String(m.died || '').trim();
      if (!year) return;
      if (!byYear[year]) byYear[year] = [];
      byYear[year].push(m);
    });

    const years = Object.keys(byYear).sort((a, b) => Number(b) - Number(a));

    root.innerHTML = '';
    years.forEach((year, yearIdx) => {
      const group = document.createElement('div');
      group.className = 'memorial-year-group';

      const label = document.createElement('div');
      label.className = 'memorial-year-label';
      label.textContent = year;
      group.appendChild(label);

      const grid = document.createElement('div');
      grid.className = 'memorial-grid reveal-stagger';

      byYear[year].forEach(m => {
        const card = document.createElement('article');
        card.className = 'memorial-card reveal';

        const name = document.createElement('h3');
        name.className = 'memorial-card-name';
        name.textContent = m.name || '';
        card.appendChild(name);

        const dates = document.createElement('p');
        dates.className = 'memorial-card-dates';
        dates.innerHTML = `${m.born || ''} <span class="dash">—</span> ${m.died || ''}`;
        card.appendChild(dates);

        if (m.note) {
          const note = document.createElement('p');
          note.className = 'memorial-card-note';
          note.textContent = `"${m.note}"`;
          card.appendChild(note);
        }

        grid.appendChild(card);
      });

      group.appendChild(grid);
      root.appendChild(group);
    });

    // Re-attach reveal observer to the freshly inserted cards
    const observer = new IntersectionObserver((entries) => {
      entries.forEach(entry => {
        if (entry.isIntersecting) {
          entry.target.classList.add('visible');
          observer.unobserve(entry.target);
        }
      });
    }, { threshold: 0.12, rootMargin: '0px 0px -40px 0px' });
    root.querySelectorAll('.reveal').forEach(el => observer.observe(el));

  } catch (err) {
    root.innerHTML = `<p class="memorial-empty">${t('memorial.error')}</p>`;
  }
}

// === DEMO MODAL ===
function openModal() {
  const backdrop = document.getElementById('vipps-modal');
  if (!backdrop) return;
  backdrop.classList.add('open');
  document.body.style.overflow = 'hidden';
  backdrop.querySelector('.modal-close-btn')?.focus();
}

function closeModal() {
  const backdrop = document.getElementById('vipps-modal');
  if (!backdrop) return;
  backdrop.classList.remove('open');
  document.body.style.overflow = '';
}

function initModal() {
  const backdrop = document.getElementById('vipps-modal');
  if (!backdrop) return;

  backdrop.querySelector('.modal-close-btn')?.addEventListener('click', closeModal);
  backdrop.addEventListener('click', e => { if (e.target === backdrop) closeModal(); });
  document.addEventListener('keydown', e => {
    if (e.key === 'Escape' && backdrop.classList.contains('open')) closeModal();
  });
}

// === CONTACT FORM ===
function initContactForm() {
  const form = document.getElementById('contact-form');
  if (!form) return;

  form.addEventListener('submit', e => {
    e.preventDefault();
    openModal();
    form.reset();
  });
}

